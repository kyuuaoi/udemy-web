// Desafio:

// Calcule a área da circunferência,
// imprima o resultado no log,
// crie novas "lets"

// PI * raio * raio

let raio = 10;

// Resposta:

const PI = 3.14;
const area = (PI * raio * raio);

console.log ("O valor da área é: " + area + "m².");

const areaMathPI = (Math.PI * raio * raio);

console.log ("O valor da área com Math.PI é: " + areaMathPI + "m².");